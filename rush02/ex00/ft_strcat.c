#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	char	*j;

	j = dest;
	while (*j != '\0')
	{
		j++;
	}
	while (*src != '\0')
	{
		*j++ = *src++;
	}
	*j = '\0';
	return (dest);
}
